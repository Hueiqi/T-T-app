class _DashboardTab extends StatefulWidget {
  final GlobalKey? dashboardKey;
  final List<QuickTourStep> tourSteps;
  final GlobalKey aiChatKey;

  const _DashboardTab({
    this.dashboardKey,
    required this.tourSteps,
    required this.aiChatKey,
  });

  @override
  State<_DashboardTab> createState() => _DashboardTabState();
}

class _DashboardTabState extends State<_DashboardTab>
    with WidgetsBindingObserver {
  final _quickTourKey = GlobalKey<QuickTourState>();
  bool _showTourPrompt = false;
  bool _isDashboardLoading = true;
  List<Map<String, dynamic>> _calorieHistory = [];
  String _weightFilter = 'All';
  String _reportPeriod = 'This Week';
  List<WeightEntry> _weightHistory = [];
  List<Meal> _reportMeals = [];

  @override
  void initState() {
    super.initState();
    WidgetsBinding.instance.addObserver(this);
    WidgetsBinding.instance.addPostFrameCallback((_) => _loadData());
  }

  @override
  void dispose() {
    WidgetsBinding.instance.removeObserver(this);
    super.dispose();
  }

  Future<void> _loadData() async {
    setState(() => _isDashboardLoading = true);
    final auth = context.read<AuthProvider>();
    if (auth.user == null) {
      setState(() => _isDashboardLoading = false);
      return;
    }

    try {
      final prefs = await SharedPreferences.getInstance();
      final localDone = prefs.getBool('quick_tour_done') ?? false;
      final userDone = auth.user?.hasSeenQuickTour ?? false;

      if (!userDone && !localDone) {
        if (!mounted) return;
        setState(() => _showTourPrompt = true);
        Future.delayed(const Duration(milliseconds: 600), () {
          _quickTourKey.currentState?.start();
        });
      } else if (!userDone) {
        final updated = auth.user!.copyWith(hasSeenQuickTour: true);
        await auth.updateProfile(updated);
      }

      final nutrition = context.read<NutritionProvider>();
      final health = context.read<HealthProvider>();
      final sleepProvider = context.read<SleepProvider>();
      final workoutProvider = context.read<WorkoutProvider>();
      final placeProvider = context.read<PlaceProvider>();
      final newsProvider = context.read<NewsProvider>();
      final planningProvider = context.read<PlanningProvider>();

      nutrition.initDailyCalorieGoal(auth.user!);

      await nutrition.calculateAndSetTDEE(
        user: auth.user!,
        activityLevel: auth.user!.activityLevel,
        onSave: (goal) async {
          final updated = auth.user!.copyWith(dailyCalorieTarget: goal);
          await auth.updateProfile(updated);
        },
      );

      await health.initializeHealthAccess();

      if (!mounted) return;

      await Future.wait([
        sleepProvider.loadSleepData(auth.user!.uid).catchError((e, s) => debugPrint('loadSleepData: $e')),
        nutrition.loadTodayMeals(auth.user!.uid).catchError((e, s) => debugPrint('loadTodayMeals: $e')),
        nutrition.loadWeeklyCalories(auth.user!.uid).catchError((e, s) => debugPrint('loadWeeklyCalories: $e')),
        nutrition.loadWeightHistory(auth.user!.uid).catchError((e, s) => debugPrint('loadWeightHistory: $e')),
        workoutProvider.loadDashboardData(auth.user!.uid).catchError((e, s) => debugPrint('loadDashboardData: $e')),
        placeProvider.loadPlaces(auth.user!.uid).catchError((e, s) => debugPrint('loadPlaces: $e')),
        newsProvider.fetchNews().catchError((e, s) => debugPrint('fetchNews: $e')),
        planningProvider.loadBookmarks(auth.user!.uid).catchError((e, s) => debugPrint('loadBookmarks: $e')),
      ]);

      _weightHistory = nutrition.weightHistory;

      final recentMeals = await nutrition.loadRecentMeals(auth.user!.uid, days: 30);
      _computeCalorieHistory(recentMeals, workoutProvider.workouts);
      await _loadReportMeals(nutrition, auth.user!.uid);

      if (!mounted) return;
      await _syncHealthConnectSteps(auth.user!.uid);
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(
          context,
        ).showSnackBar(SnackBar(content: Text('Error loading data: $e')));
      }
    } finally {
      if (mounted) {
        setState(() {
          _isDashboardLoading = false;
        });
      }
    }
  }

  void _saveTodaySteps() {
    final uid = context.read<AuthProvider>().user?.uid;
    final steps = context.read<MotionProvider>().stepsToday;
    if (uid != null) {
      FirebaseService().saveDailySteps(uid, DateTime.now(), steps);
    }
  }

  Future<void> _syncHealthConnectSteps(String uid) async {
    try {
      final hc = HealthConnectService();
      if (!await hc.isAvailable) return;
      final steps = await hc.getStepsToday();
      if (steps > 0) {
        await FirebaseService().saveDailySteps(uid, DateTime.now(), steps);
      }
    } catch (e) {
      debugPrint('HealthConnect sync error: $e');
    }
  }

  void _computeCalorieHistory(List<Meal> meals, List<Workout> workouts) {
    final Map<String, double> eatenByDay = {};
    final Map<String, double> burnedByDay = {};

    for (final meal in meals) {
      final key = '${meal.dateTime.year}-${meal.dateTime.month.toString().padLeft(2, '0')}-${meal.dateTime.day.toString().padLeft(2, '0')}';
      eatenByDay[key] = (eatenByDay[key] ?? 0) + meal.calories;
    }

    for (final workout in workouts) {
      final key = '${workout.startTime.year}-${workout.startTime.month.toString().padLeft(2, '0')}-${workout.startTime.day.toString().padLeft(2, '0')}';
      burnedByDay[key] = (burnedByDay[key] ?? 0) + workout.caloriesBurned;
    }

    final history = <Map<String, dynamic>>[];
    final now = DateTime.now();
    for (int i = 29; i >= 0; i--) {
      final date = now.subtract(Duration(days: i));
      final key = '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
      history.add({
        'date': date,
        'eaten': eatenByDay[key] ?? 0.0,
        'burned': burnedByDay[key] ?? 0.0,
      });
    }

    setState(() => _calorieHistory = history);
  }

  List<Map<String, dynamic>> _getSevenDayCalorieData(
    NutritionProvider nutrition,
    WorkoutProvider workout,
  ) {
    final List<Map<String, dynamic>> result = [];
    final now = DateTime.now();

    final weeklyCalories = nutrition.weeklyCalories;

    final workoutsByDate = <String, double>{};
    for (final w in workout.workouts) {
      if (w.endTime == null) continue;
      final dateKey = '${w.endTime!.year}-${w.endTime!.month.toString().padLeft(2, '0')}-${w.endTime!.day.toString().padLeft(2, '0')}';
      workoutsByDate[dateKey] = (workoutsByDate[dateKey] ?? 0) + w.caloriesBurned;
    }

    for (int i = 6; i >= 0; i--) {
      final date = now.subtract(Duration(days: i));
      final key = '${date.year}-${date.month.toString().padLeft(2, '0')}-${date.day.toString().padLeft(2, '0')}';
      final eaten = weeklyCalories[key] ?? 0;
      final burned = workoutsByDate[key] ?? 0;
      result.add({
        'date': date,
        'eaten': eaten,
        'burned': burned,
        'day': ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'][date.weekday - 1],
      });
    }
    return result;
  }

  Widget _buildSevenDayChart(List<Map<String, dynamic>> data) {
    if (data.isEmpty) return const SizedBox.shrink();

    final maxVal = data.fold<double>(
      0,
      (max, entry) => max > (entry['eaten'] as double) ? max : (entry['eaten'] as double),
    );
    final maxBurned = data.fold<double>(
      0,
      (max, entry) => max > (entry['burned'] as double) ? max : (entry['burned'] as double),
    );
    final overallMax = maxVal > maxBurned ? maxVal : maxBurned;

    return Card(
      child: Padding(
        padding: const EdgeInsets.all(16),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            const Text(
              '7-Day Calorie History',
              style: TextStyle(fontWeight: FontWeight.bold, fontSize: 16),
            ),
            const SizedBox(height: 12),
            SizedBox(
              height: 200,
              child: BarChart(
                BarChartData(
                  gridData: const FlGridData(show: false),
                  titlesData: FlTitlesData(
                    leftTitles: AxisTitles(
                      sideTitles: SideTitles(
                        showTitles: true,
                        reservedSize: 40,
                        getTitlesWidget: (value, meta) => Text(
                          '${value.toInt()}',
                          style: const TextStyle(fontSize: 10),
                        ),
                      ),
                    ),
                    bottomTitles: AxisTitles(
                      sideTitles: SideTitles(
                        showTitles: true,
                        getTitlesWidget: (value, meta) {
                          final idx = value.toInt();
                          if (idx >= 0 && idx < data.length) {
                            return Text(
                              data[idx]['day'] as String,
                              style: const TextStyle(fontSize: 10),
                            );
                          }
                          return const Text('');
                        },
                      ),
                    ),
                    rightTitles: const AxisTitles(sideTitles: SideTitles(showTitles: false)),
                    topTitles: const AxisTitles(sideTitles: SideTitles(showTitles: false)),
                  ),
                  borderData: FlBorderData(show: false),
                  barGroups: data.asMap().entries.map((entry) {
                    final idx = entry.key;
                    final item = entry.value;
                    final eaten = item['eaten'] as double;
                    final burned = item['burned'] as double;
                    return BarChartGroupData(
                      x: idx,
                      barRods: [
                        BarChartRodData(
                          toY: eaten,
                          color: AppTheme.warningColor,
                          width: 12,
                          borderRadius: BorderRadius.circular(4),
                        ),
                        BarChartRodData(
                          toY: burned,
                          color: AppTheme.primaryColor,
                          width: 12,
                          borderRadius: BorderRadius.circular(4),
                        ),
                      ],
                      barsSpace: 4,
                    );
                  }).toList(),
                  minY: 0,
                  maxY: overallMax * 1.2,
                  barTouchData: BarTouchData(
                    touchTooltipData: BarTouchTooltipData(
                      getTooltipItem: (group, groupIndex, rod, rodIndex) {
                        final label = rodIndex == 0 ? 'Eaten' : 'Burned';
                        return BarTooltipItem(
                          '$label: ${rod.toY.toStringAsFixed(0)} kcal',
                          const TextStyle(color: Colors.white, fontSize: 12),
                        );
                      },
                    ),
                  ),
                ),
              ),
            ),
            const SizedBox(height: 8),
            Row(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                _legendItem('Eaten', AppTheme.warningColor),
                const SizedBox(width: 16),
                _legendItem('Burned', AppTheme.primaryColor),
              ],
            ),
          ],
        ),
      ),
    );
  }

