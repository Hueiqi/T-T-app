  Future<void> _loadReportMeals(NutritionProvider nutrition, String userId) async {
    final now = DateTime.now();
    DateTime startDate;
    switch (_reportPeriod) {
      case 'This Week':
        startDate = now.subtract(Duration(days: now.weekday - 1));
        break;
      case 'This Month':
        startDate = DateTime(now.year, now.month, 1);
        break;
      case 'All Time':
        startDate = DateTime(2000, 1, 1);
        break;
      default:
        startDate = now.subtract(const Duration(days: 7));
    }
    await nutrition.loadMealsForDateRange(userId, startDate, now);
    _reportMeals = nutrition.dateRangeMeals;
  }

  List<WeightEntry> _getFilteredWeights() {
    final now = DateTime.now();
    List<WeightEntry> filtered = _weightHistory.where((e) {
      switch (_weightFilter) {
        case '1M':
          return e.date.isAfter(now.subtract(const Duration(days: 30)));
        case '6M':
          return e.date.isAfter(now.subtract(const Duration(days: 180)));
        case '1Y':
          return e.date.isAfter(now.subtract(const Duration(days: 365)));
        default:
          return true;
      }
    }).toList();
    filtered.sort((a, b) => a.date.compareTo(b.date));
    return filtered;
  }

